#A for loop is used for iterating over a sequence (that is either a list, a tuple, a dictionary, a set, or a string).

fruits = ['apple', 'Strawberry', 'pineaple']

for fruit in fruits:
    print(fruit)

for letter in 'apple':
    print(letter)

item = fruits[1]
for letter in item:
    print(letter)
    
    
